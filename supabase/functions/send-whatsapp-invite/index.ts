
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface WhatsAppInviteRequest {
  phone: string;
  inviteUrl: string;
  roleName: string;
  expiresAt: string;
  senderName?: string;
  notes?: string;
  inviteId?: string;
  email?: string;
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const supabase = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    )

    const { phone, inviteUrl, roleName, expiresAt, senderName, notes, inviteId, email }: WhatsAppInviteRequest = await req.json()

    console.log('üö® [WHATSAPP-DEBUG] FUN√á√ÉO CHAMADA! Dados recebidos:', {
      phone: phone?.substring(0, 5) + '***',
      hasInviteUrl: !!inviteUrl,
      roleName,
      email: email?.substring(0, 5) + '***',
      timestamp: new Date().toISOString()
    })

    console.log('üì± [WHATSAPP] Iniciando envio de convite para:', phone?.substring(0, 5) + '***')
    console.log('üì± [WHATSAPP] URL do convite:', inviteUrl)
    console.log('üì± [WHATSAPP] Role:', roleName)
    console.log('üì± [WHATSAPP] Email:', email?.substring(0, 5) + '***')

    // Validar dados obrigat√≥rios
    if (!phone || !inviteUrl) {
      console.error('‚ùå [WHATSAPP] Dados obrigat√≥rios faltando:', { phone: !!phone, inviteUrl: !!inviteUrl })
      throw new Error('Telefone e URL do convite s√£o obrigat√≥rios')
    }

    // Processar n√∫mero de telefone internacional
    let formattedPhone = phone.replace(/\D/g, '')
    
    // Se o telefone est√° no formato internacional "+dialCode|phoneNumber"
    if (phone.includes('|')) {
      const [dialCode, phoneNumber] = phone.split('|')
      const cleanDialCode = dialCode.replace('+', '')
      const cleanPhoneNumber = phoneNumber.replace(/\D/g, '')
      formattedPhone = cleanDialCode + cleanPhoneNumber
    }
    // Se j√° tem c√≥digo de pa√≠s (n√∫mero longo), usar como est√°
    else if (formattedPhone.length > 11) {
      // J√° tem c√≥digo do pa√≠s
      formattedPhone = formattedPhone
    }
    // Se √© n√∫mero brasileiro sem c√≥digo
    else if (formattedPhone.length === 11 || formattedPhone.length === 10) {
      formattedPhone = '55' + formattedPhone
    }

    console.log('üì± [WHATSAPP] Telefone formatado:', formattedPhone)

    // Extrair nome do email da pessoa CONVIDADA (n√£o do sender)
    const userName = email ? email.split('@')[0].replace(/[._-]/g, ' ').trim() : 'Novo Membro'
    
    console.log('üì± [WHATSAPP] Nome do usu√°rio para template:', userName)

    // Dados para o template do WhatsApp (template "convitevia" precisa de 2 par√¢metros)
    const templateData = {
      messaging_product: "whatsapp",
      to: formattedPhone,
      type: "template",
      template: {
        name: "convitevia",
        language: {
          code: "pt_BR"
        },
        components: [
          {
            type: "body",
            parameters: [
              {
                type: "text",
                text: userName
              },
              {
                type: "text", 
                text: inviteUrl
              }
            ]
          }
        ]
      }
    }

    // Verificar credenciais do Supabase Secrets
    const whatsappToken = Deno.env.get('WHATSAPP_BUSINESS_TOKEN')
    const phoneNumberId = Deno.env.get('WHATSAPP_BUSINESS_PHONE_ID')

    console.log('üîë [WHATSAPP] Verifica√ß√£o de credenciais:', { 
      hasToken: !!whatsappToken, 
      tokenLength: whatsappToken?.length,
      hasPhoneId: !!phoneNumberId,
      phoneIdLength: phoneNumberId?.length
    })

    if (!whatsappToken || !phoneNumberId) {
      console.error('‚ùå [WHATSAPP] Credenciais n√£o configuradas no Supabase Secrets:', { 
        hasToken: !!whatsappToken, 
        hasPhoneId: !!phoneNumberId 
      })
      
      // Registrar tentativa falhada para auditoria
      if (inviteId) {
        try {
          await supabase.from('invite_deliveries').insert({
            invite_id: inviteId,
            channel: 'whatsapp',
            status: 'failed',
            error_message: 'Credenciais WhatsApp n√£o configuradas no Supabase Secrets',
            metadata: { phone: formattedPhone, error_type: 'missing_credentials' }
          })
        } catch (logError) {
          console.error('‚ö†Ô∏è Erro ao registrar falha:', logError)
        }
      }
      
      throw new Error('Credenciais do WhatsApp Business n√£o configuradas no Supabase Secrets. Configure WHATSAPP_BUSINESS_TOKEN e WHATSAPP_BUSINESS_PHONE_ID nos Edge Function Secrets.')
    }

    console.log('üì± [WHATSAPP] Enviando template via API...')
    console.log('üì± [WHATSAPP] Phone Number ID:', phoneNumberId)
    console.log('üì± [WHATSAPP] Template data:', JSON.stringify(templateData, null, 2))

    // Criar timeout e abort controller para evitar travamentos
    const controller = new AbortController()
    const timeoutId = setTimeout(() => controller.abort(), 30000) // 30 segundos

    let whatsappResponse
    try {
      whatsappResponse = await fetch(
        `https://graph.facebook.com/v18.0/${phoneNumberId}/messages`,
        {
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${whatsappToken}`,
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(templateData),
          signal: controller.signal
        }
      )
    } catch (fetchError: any) {
      clearTimeout(timeoutId)
      
      // Verificar se foi timeout ou erro de rede
      if (fetchError.name === 'AbortError') {
        console.error('‚ùå [WHATSAPP] Timeout na requisi√ß√£o (30s)')
        throw new Error('Timeout na API do WhatsApp - tente novamente')
      }
      
      console.error('‚ùå [WHATSAPP] Erro de conectividade:', fetchError.message)
      throw new Error(`Erro de conectividade: ${fetchError.message}`)
    } finally {
      clearTimeout(timeoutId)
    }

    const whatsappResult = await whatsappResponse.json()

    console.log('üì± [WHATSAPP] Resposta completa:', {
      status: whatsappResponse.status,
      statusText: whatsappResponse.statusText,
      result: whatsappResult
    })

    if (!whatsappResponse.ok) {
      const errorMsg = `Erro ${whatsappResponse.status}: ${whatsappResult.error?.message || whatsappResult.message || 'Erro desconhecido'}`
      console.error('‚ùå [WHATSAPP] Erro da API:', errorMsg)
      console.error('‚ùå [WHATSAPP] Detalhes do erro:', whatsappResult)
      
      // Registrar falha detalhada para auditoria
      if (inviteId) {
        try {
          await supabase.from('invite_deliveries').insert({
            invite_id: inviteId,
            channel: 'whatsapp',
            status: 'failed',
            error_message: errorMsg,
            metadata: { 
              phone: formattedPhone, 
              http_status: whatsappResponse.status,
              api_error: whatsappResult,
              error_type: 'api_error'
            }
          })
        } catch (logError) {
          console.error('‚ö†Ô∏è Erro ao registrar falha da API:', logError)
        }
      }
      
      throw new Error(errorMsg)
    }

    // Registrar sucesso e atualizar estat√≠sticas
    const messageId = whatsappResult.messages?.[0]?.id

    if (inviteId) {
      try {
        // Registrar entrega bem-sucedida
        await supabase.from('invite_deliveries').insert({
          invite_id: inviteId,
          channel: 'whatsapp',
          status: 'sent',
          sent_at: new Date().toISOString(),
          provider_id: messageId,
          metadata: { 
            phone: formattedPhone,
            message_id: messageId,
            template_used: 'convitevia'
          }
        })

        // Atualizar estat√≠sticas do convite
        const { error: updateError } = await supabase.rpc('update_invite_send_attempt', {
          invite_id: inviteId
        })

        if (updateError) {
          console.error('‚ö†Ô∏è Erro ao atualizar estat√≠sticas do convite (n√£o cr√≠tico):', updateError)
        } else {
          console.log('‚úÖ Estat√≠sticas do convite atualizadas com sucesso')
        }
      } catch (statError) {
        console.error('‚ö†Ô∏è Erro n√£o cr√≠tico ao atualizar estat√≠sticas:', statError)
        // N√£o falhar o envio por causa das estat√≠sticas
      }
    }

    console.log('‚úÖ Convite enviado via WhatsApp com sucesso! Message ID:', messageId)

    return new Response(JSON.stringify({
      success: true,
      message: 'Convite enviado via WhatsApp com sucesso',
      whatsappId: messageId,
      strategy: 'whatsapp_template',
      method: 'whatsapp_business_api',
      phone: formattedPhone
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 200,
    })

  } catch (error) {
    console.error('‚ùå Erro ao enviar convite via WhatsApp:', error)

    return new Response(JSON.stringify({
      success: false,
      message: 'Erro ao enviar convite via WhatsApp',
      error: error.message,
      suggestion: 'Verifique o n√∫mero de telefone e tente novamente'
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 500,
    })
  }
})
